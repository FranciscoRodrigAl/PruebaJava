    private void bt_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(this.tf_codigo.getText().trim());
        Empleado emp = new Empleado();
        try {
            emp.setCodigo(codigo);
            boolean resultado = emp.eliminar();
            if(resultado){
                JOptionPane.showMessageDialog(this,"empleado eliminado");
            }else{
                JOptionPane.showMessageDialog(this,"empleado no pudo eliminarse");
            }
        } catch (Exception ex) {
            Logger.getLogger(jf_eliminar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bt_eliminarActionPerformed

