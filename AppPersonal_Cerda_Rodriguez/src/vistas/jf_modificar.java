    private void bt_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_buscarActionPerformed
        // TODO add your handling code here:
        codigo = Integer.parseInt(this.tf_codigo.getText().trim());
        if(!this.tf_codigo.getText().trim().equals("")){
            if(codigo > 0 && codigo <= 100){
                Empleado emp = new Empleado();
                try {
                    System.out.println(codigo);
                    emp.setCodigo(codigo);
                    emp = emp.recuperar();
                    desplegarEnFormulario(emp);

                } catch (Exception ex) {
                    Logger.getLogger(jf_modificar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ingrese un valor valido mayor a 0 y menor o igual a 100");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe ingresar un valor para la busqueda del codigo");
        }
    }//GEN-LAST:event_bt_buscarActionPerformed

    private void bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_agregarActionPerformed
        // TODO add your handling code here:
        RUN = this.tf_run.getText().trim();
        nombre = this.tf_nombre.getText().trim();
        apellido = this.tf_apellido.getText().trim();
        celular = Integer.parseInt(this.tf_celular.getText().trim());
        email = this.tf_email.getText().trim();
        sueldo = Integer.parseInt(this.tf_sueldo.getText().trim());
        estadoCivil = (char) this.cb_estado_civil.getSelectedItem().toString().charAt(0);
        depto = this.cb_departamento.getSelectedItem().toString();

        if (codigo > 0 && codigo <= 100) {
            if (!RUN.equals("")) {
                if (!nombre.equals("")) {
                    if (!apellido.equals("")) {
                        if (String.valueOf(celular).length() == 9) {
                            if (sueldo >= 120000) {
                                if ((!this.cb_estado_civil.getSelectedItem().equals("Seleccione")) || estadoCivil == 'C' || estadoCivil == 'S' || estadoCivil == 'V') {
                                    if (!depto.equals("Seleccione")) {
                                        try {
                                            Empleado emp = new Empleado();
                                            emp.setCodigo(codigo);
                                            emp.setRut(RUN);
                                            emp.setNombre(nombre);
                                            emp.setApellido(apellido);
                                            emp.setCelular(celular);
                                            emp.setEmail(email);
                                            emp.setSueldoBruto(sueldo);
                                            emp.setEstadoCivil(String.valueOf(estadoCivil));
                                            emp.setNombreDepartamento(depto);
                                            
                                            boolean resultado =  emp.modificar();
                                            if(resultado){
                                                JOptionPane.showMessageDialog(this, "Empleado modificado");
                                            }else{
                                                JOptionPane.showMessageDialog(this, "Empleado no pudo modificarse");
                                            }
                                        } catch (Exception ex) {
                                            Logger.getLogger(jf_agregar.class.getName()).log(Level.SEVERE, null, ex);
                                            JOptionPane.showMessageDialog(this, "Excepcion en el proceso");
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(this, "Debe ingresar el departamento correspondiente");
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(this, "El estaod civil ingresado debe ser ingresado");
                                }
                            } else {
                                JOptionPane.showMessageDialog(this, "El sueldo ingresado debe ser de un mínimo de $120.000");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "El celular ingresado debe contener 9 digitos");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "El apellido ingresado no puede estar en blanco");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "El nombre ingresado no puede estar en blanco");
                }
            } else {
                JOptionPane.showMessageDialog(this, "El RUN ingresado no puede estar en blanco");
            }
        } else {
            JOptionPane.showMessageDialog(this, "El codigo ingresado debe ser superior a 0 y menor o igual a 100");
        }
        
    }//GEN-LAST:event_bt_agregarActionPerformed

