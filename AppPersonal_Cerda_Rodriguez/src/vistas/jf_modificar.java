    public void desplegarEnFormulario(Empleado empleado){
        //set de propiedades en variables
        
        //variables a campos
        this.tf_run.setText(empleado.getRut());
        this.tf_nombre.setText(empleado.getNombre());
        this.tf_apellido.setText(empleado.getApellido());
        this.tf_celular.setText(String.valueOf(empleado.getCelular()));
        this.tf_email.setText(empleado.getEmail());
        this.tf_sueldo.setText(String.valueOf(empleado.getSueldoBruto()));
        this.cb_estado_civil.setSelectedItem(String.valueOf(empleado.getEstadoCivil()));
        this.cb_departamento.setSelectedItem(String.valueOf(empleado.getNombreDepartamento()));
    }
    
